pub fn load_hex(vm: &mut AvrVm, file_path: &str) -> io::Result<()> {
    let file = File::open(file_path)?;
    let reader = BufReader::new(file);

    let mut ext_addr: u32 = 0;

    for line in reader.lines() {
        let line = line?;
        if line.is_empty() || !line.starts_with(':') {
            continue;
        }

        let bytes: Vec<u8> = (1..line.len())
            .step_by(2)
            .filter_map(|i| u8::from_str_radix(&line[i..i + 2], 16).ok())
            .collect();

        if bytes.len() < 5 {
            continue;
        }

        let count = bytes[0] as usize;
        let addr = ((bytes[1] as u32) << 8) | (bytes[2] as u32);
        let record_type = bytes[3];

        let mut checksum: u8 = 0;
        for &b in &bytes[..bytes.len() - 1] {
            checksum = checksum.wrapping_add(b);
        }
        checksum = (!checksum).wrapping_add(1);

        if checksum != *bytes.last().unwrap() {
            println!("Checksum error in hex file.");
            continue;
        }

        match record_type {
            0x00 => { // Data
                let full_addr = ext_addr + addr;
                for i in 0..count {
                    if ((full_addr + i as u32) as usize) < vm.flash.len() {
                        vm.flash[(full_addr + i as u32) as usize] = bytes[4 + i];
                    }
                }
            }
            0x01 => break, // EOF
            0x02 => { // Extended Segment Address
                if count >= 2 {
                    ext_addr = (((bytes[4] as u32) << 8) | (bytes[5] as u32)) << 4;
                }
            }
            0x04 => { // Extended Linear Address
                if count >= 2 {
                    ext_addr = (((bytes[4] as u32) << 8) | (bytes[5] as u32)) << 16;
                }
            }
            _ => {}
        }
    }
    Ok(())
}
